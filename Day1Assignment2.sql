1.   What does git stash do?
Git stash lets us to save our uncommitted changes and return to a clean working directory.we can come back and restore our changes later.

2. What does git log show?
Git Log is one of the most powerful commands in Git. It allows developers to view the entire commit history of a repository - helping them track changes, review project progress, and identify when and by whom changes were made.
Git log displays a list of all commits made in a repository in reverse chronological order (latest commit first). It contains detailed information about each commit including commit hash, author name and email, date and time, commit message.

3.  What does git branch do?
In Git, a branch is like a separate workspace where you can make changes and try new ideas without affecting the main project.Branches let you work on different parts of a project, like new features or bug fixes, without interfering with the main branch.

4.  What does git checkout do?
The git checkout command is one of the most versatile commands in Git. It allows you to switch between branches, create new branches, and even revert changes in your working directory. 
While its primary use is branch management, git checkout can also be used to navigate to specific commits and manipulate individual files across branches.

5.  How to implement merging in git? 
Merging is Git's way of putting a forked history back together again. The git merge command lets you take the independent lines of development created by git branch and integrate them into a single branch.

6. Diff. between  merge and rebase?
Git merge keeps the commit sequence intact by combining the histories of the two branches into a single branch. Git rebases, on the other hand, rewrite history by putting changes from one branch at the beginning of another, resulting in a more organized, linear project history.

7.  What is .gitignore used for? 
The . gitignore file tells Git which files and folders to ignore (not track). This is useful for keeping log files, temporary files, build artifacts, or personal files out of your repository. Examples of files to ignore: log files, temporary files, hidden files, personal files, OS/editor files, etc.
